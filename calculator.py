
first_number = int(input("Type first number: "))
second_number = int(input("Type second number: "))
operation = input("Type operand (+, -, *, /): ")

if operation == "+":
    print(first_number + second_number)

if operation == "-":
    print(first_number - second_number)

if operation == "*":
    print(first_number * second_number)

if operation == "/":
    print(first_number / second_number)
